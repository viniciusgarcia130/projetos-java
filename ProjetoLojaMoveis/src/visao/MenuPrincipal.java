    @Override
    public void internalFrameClosed(InternalFrameEvent ife) {
        if (ife.getInternalFrame() instanceof UICadCliente) {
            flagUICadCliente = false;
        } else if(ife.getInternalFrame() instanceof UICadFuncionario) {
            flagUICadFuncionario = false;
        } else if(ife.getInternalFrame() instanceof UICadMovel){
            flagUICadMovel = false;
        } else if(ife.getInternalFrame() instanceof UIManutencaoCliente){
            flagUIManutencaoCliente = false;
        } else if(ife.getInternalFrame() instanceof UIManutencaoFuncionario){
            flagUIManutencaoFuncionario = false;
        } else if(ife.getInternalFrame() instanceof UIManutencaoMovel){
            flagUIManutencaoMovel = false;
        }
    }

