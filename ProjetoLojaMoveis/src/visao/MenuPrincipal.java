    private void abrirCadMovel(){
        if (!flagUICadMovel) {
            UICadMovel ucm =  new UICadMovel();
            jAreaTrabalho.add(ucm);
            ucm.setVisible(true);
            flagUICadMovel = true;
            ucm.addInternalFrameListener(this);
        }
    }
    
